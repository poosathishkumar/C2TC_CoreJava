package com.cg.daytwo;

public class ArithmeticOperators {

	public static void main(String[] args) {
			int a=25,b=16;
			float c;
			c=a+b;
			System.out.println("ADD c= "+c);
			
			c=a-b;
			System.out.println("SUB c= "+c);
			
			c=a*b;
			System.out.println("mul c= "+c);
			
			
			c=a/b;
			System.out.println("diff c= "+c);
			
			c=a%b;
			System.out.println("rem c= "+c);
			
			
			
			
			
			
	}

}
